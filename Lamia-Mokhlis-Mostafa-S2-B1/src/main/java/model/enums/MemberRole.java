package model.enums;

/**
 * Represents the different roles a member can have in a project.
 */
public enum MemberRole {
    /**
     * Represents the role of a project manager.
     */
    PROJECT_MANAGER,

    /**
     * Represents the role of a developer.
     */
    DEVELOPER,

    /**
     * Represents the role of a designer.
     */
    DESIGNER;
}

